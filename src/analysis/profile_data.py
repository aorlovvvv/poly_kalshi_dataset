from __future__ import annotations

import json
import os
import re
import glob as globmod
from pathlib import Path
from typing import Any

import duckdb
import yaml
from dotenv import load_dotenv

load_dotenv()
ROOT = Path(__file__).resolve().parents[2]


def load_yaml(path: Path) -> dict[str, Any]:
    return yaml.safe_load(path.read_text(encoding="utf-8"))


def abs_glob(g: str) -> str:
    p = Path(g)
    if p.is_absolute():
        return g
    return (ROOT / g).as_posix()


def bytes_to_gb(n: int) -> float:
    return float(n) / (1024**3)


def guess_roles(colnames: list[str]) -> dict[str, list[str]]:
    def pick(patterns: list[str]) -> list[str]:
        out = []
        for c in colnames:
            lc = c.lower()
            if any(re.search(p, lc) for p in patterns):
                out.append(c)
        return out

    return {
        "timestamp_candidates": pick([r"timestamp", r"\bts\b", r"created", r"datetime", r"block_?time", r"time"]),
        "price_candidates": pick([r"\bprice\b", r"prob", r"implied", r"\brate\b"]),
        "quantity_candidates": pick([r"\bsize\b", r"amount", r"qty", r"quantity", r"shares", r"contracts", r"\bvolume\b"]),
        "market_id_candidates": pick([r"market", r"condition", r"token", r"event", r"ticker", r"\bid\b"]),
    }


def main() -> None:
    cfg = load_yaml(ROOT / "config" / "project.yml")
    ds_cfg_path = ROOT / cfg["datasets_config"]
    ds_cfg = load_yaml(ds_cfg_path)

    con = duckdb.connect()

    out_dir = ROOT / "artifacts"
    out_dir.mkdir(parents=True, exist_ok=True)

    catalog: dict[str, Any] = {"datasets": {}}

    md_lines = [
        "# Data Catalog",
        "",
        "This file is auto-generated by `make profile`.",
        "",
    ]

    for name, meta in ds_cfg["datasets"].items():
        g = abs_glob(meta["glob"])

        files = globmod.glob(g, recursive=True)
        file_count = len(files)
        total_bytes = sum(os.path.getsize(f) for f in files) if files else 0

        rel = f"read_parquet('{g}', union_by_name=true)"
        cols = con.execute(f"DESCRIBE SELECT * FROM {rel}").fetchall()
        col_info = [{"name": c[0], "type": c[1]} for c in cols]
        colnames = [c["name"] for c in col_info]

        # Row count via parquet footer metadata (fast, no full scan)
        n_rows = con.execute(
            f"SELECT COALESCE(SUM(num_rows), 0) FROM parquet_file_metadata('{g}')"
        ).fetchone()[0]

        roles = guess_roles(colnames)

        sample = []
        try:
            sample = con.execute(f"SELECT * FROM {rel} LIMIT 5").fetchall()
        except Exception:
            sample = []

        catalog["datasets"][name] = {
            "platform": meta.get("platform"),
            "kind": meta.get("kind"),
            "glob": meta["glob"],
            "file_count": int(file_count),
            "total_bytes": int(total_bytes),
            "n_rows": int(n_rows),
            "n_cols": int(len(col_info)),
            "columns": col_info,
            "role_candidates": roles,
        }

        md_lines += [
            f"## {name}",
            "",
            f"- platform: **{meta.get('platform')}**",
            f"- kind: **{meta.get('kind')}**",
            f"- files: **{file_count}**",
            f"- size: **{bytes_to_gb(total_bytes):.2f} GB**",
            f"- rows (from Parquet metadata): **{n_rows}**",
            f"- cols: **{len(col_info)}**",
            "",
            "**Role candidates (heuristics):**",
            "",
            f"- timestamp: {', '.join(roles['timestamp_candidates'][:10]) or '(none found)'}",
            f"- price: {', '.join(roles['price_candidates'][:10]) or '(none found)'}",
            f"- quantity: {', '.join(roles['quantity_candidates'][:10]) or '(none found)'}",
            f"- market_id: {', '.join(roles['market_id_candidates'][:10]) or '(none found)'}",
            "",
            "**Columns:**",
            "",
        ]
        for c in col_info:
            md_lines.append(f"- `{c['name']}` — {c['type']}")
        md_lines.append("")

        if sample:
            md_lines += ["**Sample rows (first 5):**", "", "```", *[str(r) for r in sample], "```", ""]

    (out_dir / "data_catalog.json").write_text(json.dumps(catalog, indent=2), encoding="utf-8")
    (out_dir / "data_catalog.md").write_text("\n".join(md_lines), encoding="utf-8")

    def macro_key(s: str) -> str:
        parts = re.split(r"[^a-zA-Z0-9]+", s)
        return "".join(p[:1].upper() + p[1:] for p in parts if p)

    lines = ["% Auto-generated. Do not edit by hand."]
    for ds_name, info in catalog["datasets"].items():
        key = macro_key(ds_name)
        lines.append(f"\\newcommand{{\\Rows{key}}}{{{info['n_rows']}}}")
        lines.append(f"\\newcommand{{\\Files{key}}}{{{info['file_count']}}}")

    paper_dir = ROOT / "paper"
    paper_dir.mkdir(parents=True, exist_ok=True)
    (paper_dir / "results.tex").write_text("\n".join(lines) + "\n", encoding="utf-8")

    print("Wrote artifacts/data_catalog.{json,md} and paper/results.tex")


if __name__ == "__main__":
    main()
